//! ユーザー設定モデル
//!
//! このモジュールは、ユーザーの個人作業環境設定を管理するエンティティを定義します。
//! プロジェクトデータ（チーム共有）とは独立し、同じユーザーの複数端末間でのみ同期されます。
//!
//! ## 構成
//!
//! - [`tag_bookmark`] - タグブックマーク（サイドバーへのピン留め）
//!
//! ## 設計原則
//!
//! ### 個人専用
//! - 他のユーザーとは共有しない
//! - 同じユーザーの複数端末で同期
//!
//! ### プロジェクト依存
//! - 多くの設定はプロジェクトIDをキーとして管理
//! - プロジェクトごとに異なる設定を保持
//!
//! ### ローカル永続化
//! - SQLite: 高速な読み取りとクエリ
//! - Automerge: 複数端末間の同期と競合解決

